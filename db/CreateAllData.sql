-- Creation of Entity Tables
CREATE TABLE Player(
  id CHAR(20),
  name CHAR(50),
  position CHAR(50),
  nationality CHAR(50),
  num_goals INTEGER CHECK (num_goals >= 0),
  -- dob DATE,
  dominant_foot CHAR(10),
  weight INTEGER CHECK (weight > 0),
  height INTEGER CHECK (height > 0),
  PRIMARY KEY (id)
);

CREATE TABLE Stadium(
  name CHAR(50),
  city CHAR(50),
  capacity INTEGER CHECK (capacity > 0),
  -- address CHAR(30),
  PRIMARY KEY (name)
);

CREATE TABLE Manager(
  id CHAR(10),
  name CHAR(50),
  -- dob DATE,
  nationality CHAR(50),
  -- num_trophies INTEGER,
  PRIMARY KEY (id)
);

CREATE TABLE League(
  name CHAR(50),
  country CHAR(50),
  PRIMARY KEY (name)
);

CREATE TABLE Team(
  name CHAR(50),
  city CHAR(50),
  num_trophies INTEGER CHECK (num_trophies >= 0),
  since INTEGER,
  stadium_name CHAR(100),
  PRIMARY KEY (name),
  FOREIGN KEY (stadium_name) REFERENCES Stadium(name)
);

CREATE TABLE Owns(
  team_name CHAR(50),
  player_id CHAR(50),
  -- signed_from DATE,
  -- signed_to DATE,
  PRIMARY KEY (team_name, player_id),
  FOREIGN KEY (team_name) REFERENCES Team(name) ON DELETE CASCADE,
  FOREIGN KEY (player_id) REFERENCES Player(id) ON DELETE CASCADE
);

CREATE TABLE Manages(
  team_name CHAR(100),
  manager_id CHAR(50),
  managed_from DATE,
  managed_to DATE,
  -- signed_from DATE,
  -- signed_to DATE,
  PRIMARY KEY (team_name, manager_id),
  FOREIGN KEY (team_name) REFERENCES Team(name) ON DELETE CASCADE,
  FOREIGN KEY (manager_id) REFERENCES Manager(id) ON DELETE CASCADE
);

CREATE TABLE Participates(
  team_name CHAR(50),
  league_name CHAR(50),
  year INTEGER,
  PRIMARY KEY (team_name, league_name, year),
  FOREIGN KEY (team_name) REFERENCES Team(name) ON DELETE CASCADE,
  FOREIGN KEY (league_name) REFERENCES League(name) ON DELETE CASCADE
);

CREATE TABLE Match(
  id CHAR(10),
  date DATE,
  -- motm CHAR(20),
  referee CHAR(50),
  winner CHAR(50),
  PRIMARY KEY (id),
  -- FOREIGN KEY (motm) REFERENCES Player(id),
  FOREIGN KEY (winner) REFERENCES Team(name) ON DELETE CASCADE
);

CREATE TABLE Plays(
  match_id CHAR(50),
  team_name CHAR(50),
  stadium_name CHAR(50),
  team_type CHAR(50),
  goals INTEGER CHECK (goals >= 0),
  PRIMARY KEY (match_id, team_name),
  FOREIGN KEY (match_id) REFERENCES Match(id) ON DELETE CASCADE,
  FOREIGN KEY (team_name) REFERENCES Team(name) ON DELETE CASCADE,
  FOREIGN KEY (stadium_name) REFERENCES Stadium(name) ON DELETE CASCADE
);

